<template xmlns:v-el="http://www.w3.org/1999/xhtml">
    <div class="food" v-show="showFlag" transition="move" v-el:food>
        <div class="food-content">
            <div class="image-header">
                <img :src="food.image">
                <div class="back" @click="hide">
                    <i class="icon-arrow_lift"></i>
                </div>
            </div>
            <div class="content">
                <h1 class="title">{{food.name}}</h1>
                <div class="detail">
                    <span class="sell-count">月售{{food.sellCount}}份</span>
                    <span class="rating">好评率{{food.rating}}%</span>
                </div>
                <div class="price">
                    <span class="now">￥{{food.price}}</span><span class="old" v-show="food.oldPrice">￥{{food.oldPrice}}</span>
                </div>
                <div class="cartcontrol-wrapper">
                    <cartcontrol :food="food"></cartcontrol>
                </div>
                <div class="buy" @click.stop.prevent="addFirst" v-show="!food.count || food.count===0" transition="fade">加入购物车</div>
            </div>
            <split v-show="food.info"></split>
            <div class="info" v-show="food.info">
                <h1 class="title">商品介绍</h1>
                <p class="text">{{food.info}}</p>
            </div>
            <split></split>
            <div class="rating">
                <h1 class="title">商品评价</h1>
                <ratingselect :select-type="selectType" :only-content="onlyContent" :desc="desc" :ratings="food.ratings"></ratingselect>
            </div>
        </div>
    </div>
</template>

<script type="text/ecmascript-6">

    import BScroll from 'better-scroll';
    import Vue from 'vue';
    import cartcontrol from 'components/cartcontrol/cartcontrol.vue';
    import split from 'components/split/split';
    import ratingselect from 'components/ratingselect/ratingselect';

    const POSITIVE = 0;
    const NEGATIVE = 1;
    const ALL = 2;


    export default{
        props: {
            food: {
                type: Object
            }
        },
        components:{
            cartcontrol,
            split,
            ratingselect
        },
        data(){
            return {
                showFlag: false,
                selectType: ALL, // 默认选择全部
                onlyContent: true, //默认查看有内容的评论
                desc:{
                    all:"全部",
                    positive:"推荐",
                    negative:"吐槽"
                }
            }
        },
        methods: {
            addFirst(event){ // 不传参，默认的参数就是 $event
                if(!event._constructed){
                    return;
                }

                this.$dispatch('cart.add',event.target); // 点击的时候派发一个事件（传递的参数为event.target），将dom对象传递出来，实现小球飞翔动画
                Vue.set(this.food,'count',1); // 第一次，通过vue.set来设置count属性
            },
            show(){
                this.showFlag = true;

                this.selectType = ALL; // 默认选择全部
                this.onlyContent = true;

                this.$nextTick(()=>{
                    if(!this.scroll){
                        this.scroll = new BScroll(this.$els.food,{
                            click:true
                        });
                    }else{
                        this.scroll.refresh();
                    }
                });
            },
            hide(){
                this.showFlag = false;
            }
        },
        events:{

        }
    }
</script>

